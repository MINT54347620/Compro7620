public class IntegerLiteral {
   public static void main(String[] args) {
       
       System.out.println(12);//เลขฐานสิบประกอบด้วย 0-9 ห้ามขึ้นต้นด้วย 0
       System.out.println(012);//เลขฐานแปดประกอบด้วย 0-7 และขึ้นต้นด้วย 0
       System.out.println(0x12);//เลขฐานสิบประกอบด้วย 0-9 และอักขระ A-F ขึ้นต้นด้วย 0x
       
  }  
}
